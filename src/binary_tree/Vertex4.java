package binary_tree;

public class Vertex4 {

	private Vertex4 parent;
	private Vertex4 left;
	private Vertex4 right;

	private int value;
	
	public Vertex4() {

	}

	public Vertex4 getParent() {
		return parent;
	}

	public void setParent(Vertex4 parent) {
		this.parent = parent;
	}

	public Vertex4 getLeft() {
		return left;
	}

	public void setLeft(Vertex4 left) {
		this.left = left;
	}

	public Vertex4 getRight() {
		return right;
	}

	public void setRight(Vertex4 right) {
		this.right = right;
	}

	public int getValue() {
		return value;
	}

	public void setValue(int value) {
		this.value = value;
	}

}
